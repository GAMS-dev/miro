class LangParser {
  constructor(lang) {
    if (lang === 'de') {
      this.lang = de;
    } else if (lang === 'cn') {
      this.lang = cn;
    } else {
      this.lang = en;
    }
    if (!ajv.validate(schema, this.lang)) {
      throw new Error(ajv.errors);
    }
  }

  get(subkey) {
    if (subkey) {
      return this.lang[subkey];
    }
    return this.lang;
  }
}
